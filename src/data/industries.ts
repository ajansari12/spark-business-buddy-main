// Helper to get random industries for initial display
export const getRandomIndustries = (count: number = 5): Industry[] => {
  const shuffled = [...industries].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

// Helper to filter industries by tags
export const filterIndustriesByTags = (tags: string[]): Industry[] => {
  return industries.filter(industry =>
    tags.some(tag => industry.tags.includes(tag))
  );
};
